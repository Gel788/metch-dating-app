export function getRandomIcebreaker(category?: string) {
  if (category) {
    const cat = icebreakers.find(c => c.category === category)
    if (cat) {
      return cat.messages[Math.floor(Math.random() * cat.messages.length)]
    }
  }
  const allMessages = icebreakers.flatMap(c => c.messages)
  return allMessages[Math.floor(Math.random() * allMessages.length)]
}
